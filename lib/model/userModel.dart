class User {
  final int uid;
  final String name;
  final String imgUrl;
  final DateTime lastOnline;
  final String description;
  final int gender;
  final String address;
  final String UserId;

  User(this.uid, this.name, this.imgUrl, this.lastOnline, this.description,
      this.gender, this.address, this.UserId);


}